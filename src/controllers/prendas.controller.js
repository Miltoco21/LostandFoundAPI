// Actualizar estado de devoluci√≥n de una prenda - CON RESEND
export const updateEstadoDevolucion = async (req, res) => {
  console.log("=== INICIO updateEstadoDevolucion (RESEND) ===");
  
  try {
    const { id } = req.params;
    const { estado_devolucion } = req.body;

    if (!estado_devolucion) {
      return res.status(400).json({ 
        message: "El campo estado_devolucion es requerido" 
      });
    }

    console.log(`üîÑ Actualizando prenda ${id} a estado: ${estado_devolucion}`);

    // Obtener el estado anterior de la prenda ANTES de actualizar
    const [prevStateRows] = await pool.query("SELECT estado_devolucion FROM prendas WHERE id = ?", [id]);
    const previousState = prevStateRows.length > 0 ? prevStateRows[0].estado_devolucion : null;
    
    console.log(`üìä Estado anterior: ${previousState} -> Estado nuevo: ${estado_devolucion}`);

    // Actualizar el estado de devoluci√≥n y la fecha
    const updateQuery = "UPDATE prendas SET estado_devolucion = ?, fecha_devolucion = NOW() WHERE id = ?";
    const [result] = await pool.query(updateQuery, [estado_devolucion, id]);

    if (result.affectedRows === 0) {
      return res.status(404).json({ message: "Prenda no encontrada" });
    }

    console.log("‚úÖ Actualizaci√≥n exitosa. Filas afectadas:", result.affectedRows);

    // Obtener los detalles completos de la prenda actualizada
    const [garmentRows] = await pool.query("SELECT * FROM prendas WHERE id = ?", [id]);
    
    if (garmentRows.length > 0) {
      const garment = garmentRows[0];
      
      // Solo enviar email si el estado realmente cambi√≥
      if (previousState !== estado_devolucion) {
        console.log(`üìß Enviando email via Resend para cambio de estado: ${previousState} -> ${estado_devolucion}`);
        
        try {
          // Usar la funci√≥n din√°mica de emails con Resend
          const emailResult = await sendStatusUpdateEmail(
            garment.email, 
            garment, 
            estado_devolucion
          );
          
          if (emailResult.sent) {
            console.log(`‚úÖ Email enviado via Resend. ID: ${emailResult.messageId}`);
            console.log(`üìß Provider: ${emailResult.provider}`);
          } else {
            console.log(`‚ö†Ô∏è Email no enviado. Raz√≥n: ${emailResult.reason || emailResult.error}`);
          }
          
          // Incluir informaci√≥n del email en la respuesta
          res.json({ 
            message: "Estado de devoluci√≥n actualizado correctamente",
            affectedRows: result.affectedRows,
            emailStatus: {
              sent: emailResult.sent,
              provider: emailResult.provider || 'resend',
              reason: emailResult.reason,
              messageId: emailResult.messageId
            },
            estadoAnterior: previousState,
            estadoNuevo: estado_devolucion
          });
          
        } catch (emailError) {
          console.error("üí• Error al enviar email via Resend:", emailError);
          
          // El estado se actualiz√≥ correctamente, pero el email fall√≥
          res.json({ 
            message: "Estado de devoluci√≥n actualizado correctamente",
            affectedRows: result.affectedRows,
            emailStatus: {
              sent: false,
              provider: 'resend',
              error: "Error al enviar notificaci√≥n por email",
              details: emailError.message
            },
            estadoAnterior: previousState,
            estadoNuevo: estado_devolucion
          });
        }
      } else {
        console.log("üìß No se env√≠a email - el estado no cambi√≥");
        res.json({ 
          message: "Estado de devoluci√≥n actualizado correctamente",
          affectedRows: result.affectedRows,
          emailStatus: {
            sent: false,
            reason: "Estado no cambi√≥"
          }
        });
      }
    } else {
      // Esto no deber√≠a pasar, pero por seguridad
      res.json({ 
        message: "Estado de devoluci√≥n actualizado correctamente",
        affectedRows: result.affectedRows,
        emailStatus: {
          sent: false,
          reason: "No se pudo obtener informaci√≥n de la prenda para el email"
        }
      });
    }

  } catch (error) {
    console.error("üí• ERROR al actualizar estado:", error);
    return res.status(500).json({
      message: "Error interno del servidor al actualizar el estado",
      error: error.message
    });
  } finally {
    console.log("=== FIN updateEstadoDevolucion ===\n");
  }
};

